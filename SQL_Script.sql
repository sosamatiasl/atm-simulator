IF NOT EXISTS (SELECT name FROM sys.databases WHERE name = N'ATM_DB')
BEGIN
    CREATE DATABASE ATM_DB;
END
GO

USE ATM_DB;
GO

IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='Cuentas' and xtype='U')
