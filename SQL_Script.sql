    ID              INT PRIMARY KEY IDENTITY(1,1),
    NumeroCuenta    VARCHAR(16) NOT NULL UNIQUE,
    PIN             VARCHAR(4) NOT NULL,
    Saldo           DECIMAL(18, 2) NOT NULL
);
